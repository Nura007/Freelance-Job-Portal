package Project;

public class Job
{
    // attributes
    private String jobName;

    // constructor
    public Job(String jobName)
    {
        this.jobName = jobName;
    }

    // getters
    public String getJobName()
    {
        return this.jobName;
    }

    // setters
    public void setJobName(String jobName)
    {
        this.jobName = jobName;
    }
}
